//operators
//comparison operators
// ==, ===, !=, !==, >, <, >=, <=

let username = "john";
let enteredUsername = "jane";
console.log(username == enteredUsername);

//comparing strings with numbers
let age = "18";
let enteredAge = 18;
console.log(age == enteredAge); 

//implicit conversion
console.log(age == enteredAge); 

//explicit conversion
let age2 = "18";
let enteredAge2 = "18";
console.log(age2 == enteredAge2); 

//difference between == and ===
let age3 = 18;
let enteredAge3 = "18";
console.log(age3 == enteredAge3); 
console.log(age3 === enteredAge3); 

